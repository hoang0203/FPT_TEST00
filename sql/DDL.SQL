USE master
GO------
DROP DATABASE IF EXISTS FPT_TEST00;
GO------
CREATE DATABASE FPT_TEST00;
GO------
USE FPT_TEST00;
GO------
DROP SCHEMA IF EXISTS dim
GO------
CREATE SCHEMA dim
GO------
DROP SCHEMA IF EXISTS fact
GO------
CREATE SCHEMA fact
GO------
DROP TABLE IF EXISTS [dim].[CUSTOMERS]
GO------
CREATE TABLE [dim].[CUSTOMERS] (
    [CUSTOMER_ID] NVARCHAR(50) PRIMARY KEY,
    [NAME] NVARCHAR(50),
    [EMAIL] NVARCHAR(100),
    [ADDRESS] NVARCHAR(1000),
    [CREATED_AT] DATETIME DEFAULT GETDATE()
);
GO------
DROP TABLE IF EXISTS [fact].[TRANSACTIONS]
GO------
CREATE TABLE [fact].[TRANSACTIONS] (
    [TRANSACTION_ID] NVARCHAR(50) PRIMARY KEY,
    [CUSTOMER_ID] NVARCHAR(50),
    [TRANSACTION_DATE] DATE,
    [AMOUNT] FLOAT,
    [CREATED_AT] DATETIME DEFAULT GETDATE()
);
GO------
DROP TABLE IF EXISTS [dim].[PRODUCTS] 
GO------
CREATE TABLE [dim].[PRODUCTS] (
    [PRODUCT_ID] NVARCHAR(50) PRIMARY KEY,
    [PRODUCT_NAME] NVARCHAR(100),
    [CATEGORY] NVARCHAR(50),
    [PRICE] FLOAT,
    [CREATED_AT] DATETIME DEFAULT GETDATE()
);
GO------
DROP TABLE IF EXISTS [fact].[CUSTOMER_REVENUE]
GO------
CREATE TABLE [fact].[CUSTOMER_REVENUE] (
    [CUSTOMER_ID] NVARCHAR(50),
    [NAME] NVARCHAR(50),
    [EMAIL] NVARCHAR(100),
    [ADDRESS] NVARCHAR(1000),
    [TOTAL_AMOUNT] FLOAT
);